    /**
     * The main process method, which will be called by the ImageWorker in the AsyncTask background
     * thread.
     *
     * @param genotype The data to load the bitmap, in this case, a regular http URL
     * @return The downloaded and resized bitmap
     */

    protected Bitmap processBitmap(Genotype genotype) {
        if (BuildConfig.DEBUG) {
            //Log.d(TAG, "processBitmap - " + data);
        }

        // could also create arbitrary bitmap and resize it
        Bitmap b = Bitmap.createBitmap(mImageWidth, mImageHeight, Bitmap.Config.ARGB_8888);
        Canvas c = new Canvas(b);

        Paint paint = new Paint();
        paint.setARGB(255, 250, 250, 250);
        c.drawRect(0, 0, mImageWidth, mImageHeight, paint);

        SeniRuntime.render(c, genotype);

        return b;
    }





    /**
     * Load an image specified by the data parameter into an ImageView (override
     * {@link ImageWorker#processBitmap(Object)} to define the processing logic). A memory and
     * disk cache will be used if an {@link ImageCache} has been added using
     * {@link ImageWorker#addImageCache(android.app.FragmentManager, ImageCache.ImageCacheParams)}. If the
     * image is found in the memory cache, it is set immediately, otherwise an {@link AsyncTask}
     * will be created to asynchronously load the bitmap.
     *
     * @param data The URL of the image to download.
     * @param imageView The ImageView to bind the downloaded image to.
     */
    public void loadImage(Genotype genotype, ImageView imageView) {
        if (genotype == null) {
            return;
        }

        String genotypeId = genotype.getId();

        BitmapDrawable value = null;

        if (mImageCache != null) {
            value = mImageCache.getBitmapFromMemCache(genotypeId);
        }

        if (value != null) {
            // Bitmap found in memory cache
            imageView.setImageDrawable(value);
        } else if (cancelPotentialWork(genotypeId, imageView)) {
            final BitmapWorkerTask task = new BitmapWorkerTask(imageView);
            final AsyncDrawable asyncDrawable =
                    new AsyncDrawable(mResources, mLoadingBitmap, task);
            imageView.setImageDrawable(asyncDrawable);

            // NOTE: This uses a custom version of AsyncTask that has been pulled from the
            // framework and slightly modified. Refer to the docs at the top of the class
            // for more info on what was changed.
            task.executeOnExecutor(DUAL_THREAD_EXECUTOR, genotype);
            //task.executeOnExecutor(AsyncTask.SERIAL_EXECUTOR, data);
        }
    }

